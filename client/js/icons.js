class Toast {
    constructor() {
        this.container = document.createElement('div');
        this.container.className = 'toast-container';
        document.body.appendChild(this.container);
    }

    show(message, type = 'info', duration = 3000) {
        const toast = document.createElement('div');
        toast.className = `toast toast-${type}`;

        const icon = this.getIcon(type);
        const progress = document.createElement('div');
        progress.className = 'toast-progress';

        toast.innerHTML = `
      <div class="toast-icon">${icon}</div>
      <div class="toast-content">
        <div class="toast-message">${message}</div>
      </div>
      <button class="toast-close" onclick="this.parentElement.remove()">${Icons.close}</button>
      ${progress.outerHTML}
    `;

        this.container.appendChild(toast);

        const progressBar = toast.querySelector('.toast-progress');
        progressBar.style.animation = `toast-progress ${duration}ms linear forwards`;

        setTimeout(() => {
            toast.style.animation = 'toast-slide-out 0.3s ease forwards';
            setTimeout(() => toast.remove(), 300);
        }, duration);

        toast.querySelector('.toast-close').addEventListener('click', () => {
            toast.remove();
        });
    }

    getIcon(type) {
        switch (type) {
            case 'success': return Icons.check;
            case 'error': return Icons.close;
            case 'warning': return Icons.settings;
            default: return Icons.share;
        }
    }
}

window.toast = new Toast();